          <p>O Instituto Federal Catarinense (IFC) Campus Rio do Sul participa com membros efetivos da Comissão Permanente do Movimento das Feiras Catarinenses de Matemática desde 1996. Desde então, foi parceiro na formação de professores, no estudo de publicação científica dos trabalhos, na garantia do caráter público das feiras, na evolução com estudo epistemológico de avaliação em eventos, dentre outros. 
